"""Escriba un programa que calcule el área de un cuadrado cuyo lado se introduce por teclado
(El área del cuadrado es igual a lado por lado)."""

print("Introduce la medida del lado1 del rectángulo:")
lado1= float(input())
print("Introduce la medida del lado2 del rectángulo:")
lado2= float(input())
area= lado1 * lado2
print("El área del rectángulo es:", area)